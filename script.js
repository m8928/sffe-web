let currentLang = localStorage.getItem('sffe_lang') || (navigator.language.startsWith('ko') ? 'ko' : 'en');
const currentCopyVariant = resolveCopyVariant();

const themeToggleBtn = document.getElementById('theme-toggle');
const langToggleBtn = document.getElementById('lang-toggle');
const prefersDarkScheme = window.matchMedia('(prefers-color-scheme: dark)');

function resolveCopyVariant() {
    const search = new URLSearchParams(window.location.search);
    const queryValue = (search.get('copy') || '').trim().toUpperCase();
    const storedValue = (localStorage.getItem('sffe_copy_variant') || '').trim().toUpperCase();
    const queryVariant = queryValue === 'A' || queryValue === 'B' ? queryValue : null;
    const storedVariant = storedValue === 'A' || storedValue === 'B' ? storedValue : null;
    const resolved = queryVariant || storedVariant || 'A';
    localStorage.setItem('sffe_copy_variant', resolved);
    return resolved;
}

function t(lang, key) {
    const variantValue = copyVariants[currentCopyVariant]?.[lang]?.[key];
    if (typeof variantValue === 'string') {
        return variantValue;
    }
    return translations[lang]?.[key];
}

function updateAppImage() {
    const img = document.getElementById('app-screenshot');
    if (!img) {
        return;
    }

    const theme = document.documentElement.getAttribute('data-theme') || 'light';
    img.dataset.fallbackApplied = '';
    img.onerror = () => {
        if (img.dataset.fallbackApplied === '1') {
            return;
        }
        img.dataset.fallbackApplied = '1';
        img.src = `assets/images/app-ko-${theme}.webp`;
    };
    img.src = `assets/images/app-${currentLang}-${theme}.webp`;
}

function updateLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('sffe_lang', lang);
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach((element) => {
        const key = element.getAttribute('data-i18n');
        const text = t(lang, key);
        if (text) {
            element.innerHTML = text;
        }
    });

    document.querySelectorAll('[data-i18n-alt]').forEach((element) => {
        const key = element.getAttribute('data-i18n-alt');
        const text = t(lang, key);
        if (text) {
            element.setAttribute('alt', text);
        }
    });

    if (langToggleBtn) {
        langToggleBtn.textContent = lang === 'ko' ? 'EN' : 'KO';
        const langAria = t(lang, 'aria_lang_toggle');
        if (langAria) {
            langToggleBtn.setAttribute('aria-label', langAria);
        }
    }

    if (themeToggleBtn) {
        const themeAria = t(lang, 'aria_theme_toggle');
        if (themeAria) {
            themeToggleBtn.setAttribute('aria-label', themeAria);
        }
    }

    const policyLink = document.querySelector('a[data-i18n="privacy_policy"]');
    if (policyLink) {
        policyLink.href = lang === 'ko' ? 'policy-ko.html' : 'policy.html';
    }

    updateAppImage();
}

function setTheme(theme) {
    document.documentElement.setAttribute('data-theme', theme);
    localStorage.setItem('sffe_theme', theme);
    if (themeToggleBtn) {
        themeToggleBtn.textContent = theme === 'dark' ? 'LIGHT' : 'DARK';
    }
    updateAppImage();
}

function setupTheme() {
    const savedTheme = localStorage.getItem('sffe_theme');
    if (savedTheme) {
        setTheme(savedTheme);
    } else {
        setTheme(prefersDarkScheme.matches ? 'dark' : 'light');
    }

    if (themeToggleBtn) {
        themeToggleBtn.addEventListener('click', () => {
            const currentTheme = document.documentElement.getAttribute('data-theme');
            setTheme(currentTheme === 'dark' ? 'light' : 'dark');
        });
    }

    prefersDarkScheme.addEventListener('change', (event) => {
        if (!localStorage.getItem('sffe_theme')) {
            setTheme(event.matches ? 'dark' : 'light');
        }
    });
}

function setupLanguageToggle() {
    updateLanguage(currentLang);

    if (langToggleBtn) {
        langToggleBtn.addEventListener('click', () => {
            updateLanguage(currentLang === 'ko' ? 'en' : 'ko');
        });
    }
}

function setupSmoothScroll() {
    document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
        anchor.addEventListener('click', (event) => {
            const href = anchor.getAttribute('href');
            if (!href || href === '#') {
                return;
            }

            const target = document.querySelector(href);
            if (!target) {
                return;
            }

            event.preventDefault();
            target.scrollIntoView({ behavior: 'smooth', block: 'start' });
        });
    });
}

function setupRevealAnimation() {
    const revealTargets = document.querySelectorAll('.reveal');
    if (!revealTargets.length) {
        return;
    }

    if (!('IntersectionObserver' in window)) {
        revealTargets.forEach((target) => target.classList.add('is-visible'));
        return;
    }

    const observer = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
            if (entry.isIntersecting) {
                entry.target.classList.add('is-visible');
                observer.unobserve(entry.target);
            }
        });
    }, {
        threshold: 0.14,
        rootMargin: '0px 0px -30px 0px'
    });

    revealTargets.forEach((target) => observer.observe(target));
}

document.addEventListener('DOMContentLoaded', () => {
    setupTheme();
    setupLanguageToggle();
    setupSmoothScroll();
    setupRevealAnimation();
});
